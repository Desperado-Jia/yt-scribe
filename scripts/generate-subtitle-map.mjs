import { readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const rootDir = join(__dirname, '..')
const assetsDir = join(rootDir, 'assets')
const outFile = join(rootDir, 'src', 'backend', 'domain', 'video', 'hardcoded-subtitles-map.gen.ts')

const files = readdirSync(assetsDir)
  .filter(f => f.endsWith('.vtt'))
  .map(file => {
    const base = file.slice(0, -4)
    const lastDot = base.lastIndexOf('.')
    if (lastDot === -1) return null
    const videoId = base.slice(0, lastDot)
    const langCode = base.slice(lastDot + 1)
    if (!videoId || !langCode) return null
    const content = readFileSync(join(assetsDir, file), 'utf-8')
      .replace(/\\/g, '\\\\')
      .replace(/`/g, '\\`')
      .replace(/\$/g, '\\$')
    return { videoId, langCode, content }
  })
  .filter(Boolean)

const byVideoId = new Map()
for (const f of files) {
  if (!byVideoId.has(f.videoId)) byVideoId.set(f.videoId, [])
  byVideoId.get(f.videoId).push(f)
}

const entryLines = []
for (const [videoId, entries] of byVideoId) {
  const langEntries = entries.map(f => `'${f.langCode}': \`${f.content}\``).join(',\n    ')
  entryLines.push(`  '${videoId}': {\n    ${langEntries}\n  }`)
}

const content = `// Auto-generated by generate-subtitle-map.mjs. Do not edit.
export const HARDCODED_SUBTITLES_MAP: Record<string, Record<string, string>> = {
${entryLines.join(',\n')}
}
`

writeFileSync(outFile, content)
console.log(`[subtitle-map] Generated map with ${files.length} file(s), ${content.length.toLocaleString()} bytes`)
